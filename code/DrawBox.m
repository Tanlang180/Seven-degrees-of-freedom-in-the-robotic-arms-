t = [300 250 100;
    300 250 -100
    300 -250 100;
    300 -250 -100;
    100 250 100;
    100 250 -100
    100 -250 100;
    100 -250 -100];
t1 = [1 2 3 4 1 5 6 2 6 7 3 4 8 5 8 7];
i1 = 1;
x = [];
y = [];
z = [];

for i=t1
   x(i1) = t(i,1);
   y(i1) = t(i,2);
   z(i1) = t(i,3);
   i1 = i1+1;
end
plot3(x,y,z,'r'); hold on;
plot3([300 300],[250 -250],[-100 -100],'r');hold on;
plot3([100 100],[250 -250],[-100 -100],'r');hold on;
xlabel('x');ylabel('y');zlabel('z');
% view(110,10);
% view(0,0);
axis([-1300,1300,-2300,2300,-600,2300]);
grid on
